                        <li>La Méthodes Accélérée de Recherche Participative (MARP/PRA/RRA);</li>
                        <li>Les Méthodes d’évaluation et de recherche de solutions en Sécurité Alimentaire dans les pays en voie de développement à travers l’analyse (AFOM) et Connaissances, Attitudes et Pratiques (CAP).</li>
